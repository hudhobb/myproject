            <h3 align="center">พิธีกรรม</h3>
            
            
            
            <p> วันเช็งเม้งของแต่ ละปีหากเปรียบเทียบกับประเพณีไทยในทางพุทธศาสนา ก็จะเหมือนกับวันพระซึ่งใน ความเชื่อก็คือวันพระเป็นวันที่ผีหรือวิญญาณจะได้รับการปลดปล่อยให้เป็น อิสระ สามารถไปไหนมาไหนได้ จึงเป็นโอกาสดีของลูกหลานที่มีความเชื่อ  จะได้ทำพิธีกราบไหว้บูชาด้วยอาหารคาวหวาน ผลไม้ เสื้อผ้า กระดาษเงิน กระดาษ ทอง(กิมจั๊ว) น้ำชา สุรา และเงินทองของใช้ต่างๆ โดย จัดทำการเซ่นไหว้ที่แท่นบูชา ณ ที่บ้านของลูกหลานแต่ละคน ด้วยการจุดธูป บอกกล่าวเชิญกลางแจ้ง  พร้อมขออนุญาตหรือบอกธรณีประตูพาวิญญาณเข้าบ้านมาที่แท่นบูชาที่มีกระถาง ธูป และรูปถ่ายหรือป้ายวิญญาณก็ตาม โดยการเซ่นไหว้ทุกครั้งต้องบอกเจ้าที่ หรือแป๊ะเอี้ยทุกครั้ง และต้องทำช่วงเช้าไม่เกิน 5โมงเช้า เพราะหลัง จาก 5 โมงเช้าควรจุดธูปหรือกล่าวลา  พร้อม ๆ จัดการอาหารคาว ผลไม้ น้ำชา สุรา หลังจากที่ได้นำ กระดาษ (กิมจั้ว) เซ่นไหว้ไปเผาเรียบร้อยแล้ว</p>
      
        <p>  การเซ่นไหว้บรรพบุรุษที่ฮวงจุ้ยตามสุสานต่าง ๆ เอี้ยวชุน บั๊ก ซินแส กล่าวว่า ความจริงแล้วลูกหลานจะหาวันที่ดี และสะดวกแก่ทุกฝ่าย  ไม่ว่าจะเป็นฝ่ายลูกชาย หลานชาย ลูกสาว หลานสาว ที่จะมาพร้อมกันที่ฮวงจุ๊ย ทั้งเครื่องเซ่นไหว้แล้ว ซึ่งจะต้องมากันแต่เช้า ๆ เซ่นไหว้ด้วยอาหารคาว หวาน น้ำชา สุรา ผลไม้ ทุกคนที่เป็นลูกหลานยังต้องเตรียมอุปกรณ์ ไม่ว่าจะ เป็นจอบ  เสียม มีด บุ้งกี๋ สี แปรง น้ำสะอาด ซึ่งการนำอุปกรณ์นี้มาเพื่อร่วมกัน พัฒนาพื้นที่ไม่ว่าจะเป็นเอาดินขึ้นถมหลังฮวงจุ๊ย หรือล้างป้ายพร้อมทาสีให้ เกิดความสวยงาม ยังจะได้ช่วยรดน้ำพรวนดินต้นไม้บริเวณฮวงจุ๊ย และใกล้ เคียง มิใช่เพียงดูแลหญ้าที่ปลูกไว้หลังฮวงจุ๊ยเพียงอย่างเดียว  เพราะต้นไม้ที่อยู่รอบ ๆ บริเวณฮวงจุ๊ยบางต้นที่เกิดขึ้นมามีผลดี แต่บาง ต้นอาจส่งผลเสียแก่ฮวงจุ๊ยได้ ซึ่งหากท่านได้มีความเป็นคนช่างสังเกต จะเห็น ได้จากแผ่นป้ายชื่อหน้าแท่นบูชา ที่บางป้ายอาจสลักตัวหนังสือไทยไว้ด้วย</p>
      
         <p> แผ่นป้ายหินแกะสลักหรือแผ่นปูนก็ตาม ตัวหนังสือจีนตัวเล็ก ที่อยู่ทั้งสองข้าง ชื่อ ที่อยู่ และวันสร้าง ส่วนตัวหนังสือจีนใหญ่นั้นสุด แต่ว่า บรรพบุรุษที่ฝังอยู่หลุมซึ่งเป็นชาย ถ้าหากไม่มีครอบครัวจะสลักแถว เดี่ยว แต่ถ้ามีภรรยาก็จะสลักชื่อภรรยาลงไปด้วย โดยชื่อภรรยาจะอยู่ขวามือ เมื่อหันออกตามแผ่นป้าย  ถ้าภรรยาหลายคน ภรรยาหลวงจะอยู่ซ้ายมือสามีจะอยู่กลางภรรยาคนต่อไปอยู่ ขวา ตัวหนังสือเมื่อนับจากข้างบนลงล่างตัวหนังสือตัวที่  3 - 4 คือตัวชื่อสกุล ซึ่งหากใครก็ตามตายไปแล้วจะใช้สีเขียวทาตัว หนังสือ ถ้ายังไม่ตายตัวหนังสือจะต้องเป็นตัวหนังสือสีแดง เช่นเดียวกับตัว หนังสือตัวเล็กที่บอกที่อยู่ และวันสร้างฮวงจุ๊ย  ซึ่งต้องใช้สีแดงจัดการทาให้เรียบร้อย แต่ในปัจจุบันความเปลี่ยนแปลงมี มาก ตัวหนังสือทั้งหมดไม่ว่าจะเป็นชื่อคนเป็นหรือคนตายก็ตาม จะใช้สีทองหรือ ทองคำเปลวปิดหมดทุกตัวอักษร ซึ่งเป็นคนที่มีความรู้ภาษาจีนก็พอทราบได้จาก ตัวที่บอกวันเวลาสร้าง ว่าเจ้าของชื่อที่สลักไว้ตายไปแล้วหรือยัง แต่คนที่ ไม่มีความรู้ภาษาจีนอ่านไม่ได้ก็จะไม่รู้อย่างแน่นอน</p>
         
         
         
         
